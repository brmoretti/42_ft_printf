#include <stddef.h>

int	ft_memcmp(const void *s1, const void *s2, size_t n)
{
	unsigned char	*ptr1;
	unsigned char	*ptr2;

	ptr1 = (unsigned char *)s1;
	ptr2 = (unsigned char *)s2;
	while (n--)
	{
		if (*(ptr1++) != *(ptr2++))
			return (*(ptr1 - 1) - *(ptr2 - 1));
	}
	return (0);
}
